package inventorymanagement.dashboard.dashboard;

public class DashboardParams {
    int mois;
    int annee;

    public DashboardParams() {
    }

    public int getMois() {
        return mois;
    }

    public void setMois(int mois) {
        this.mois = mois;
    }

    public int getAnnee() {
        return annee;
    }

    public void setAnnee(int annee) {
        this.annee = annee;
    }
}
